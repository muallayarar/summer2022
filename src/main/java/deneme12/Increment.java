package deneme12;

public class Increment {

    public static void main(String[] args) {

        int numA = 2;
        numA = numA + 3 ;
        System.out.println(numA);

        int numB = 10;
        numB = numB*7;
        System.out.println(numB);

        int numC = 7;
        numC++;
        System.out.println(numC);

        int numD = 11;
        numD++;
        System.out.println(numD);


    }
}
